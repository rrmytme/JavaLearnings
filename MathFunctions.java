package ImportantJavaPrograms;

import java.lang.Math;
public class MathFunctions {

	public static void main(String[] args) {
	System.out.println(Math.E);
	System.out.println(Math.PI);
	System.out.println(Math.abs(-150));
	System.out.println(Math.abs(0.34343));
	System.out.println(Math.acos(10));
	System.out.println(Math.addExact(10, 12));
	System.out.println(Math.asin(10));
	System.out.println(Math.atan(10));
	System.out.println(Math.cbrt(125));
	System.out.println(Math.ceil(15.4));
	System.out.println(Math.cos(10));
	System.out.println(Math.decrementExact(11));
	System.out.println(Math.floor(10.6));
	System.out.println(Math.incrementExact(9));
	System.out.println(Math.log(10));
	System.out.println(Math.log10(10));
	System.out.println(Math.multiplyExact(10, 10));
	System.out.println(Math.max(10, 15));
	System.out.println(Math.min(10.2, 10.4));
	System.out.println(Math.negateExact(10));
	System.out.println(Math.nextAfter(10, 12));
	System.out.println(Math.nextDown(10));
	System.out.println(Math.nextUp(10));
	System.out.println(Math.pow(10, 2));
	System.out.println(Math.random());
	System.out.println(Math.round(10.76394));
	System.out.println(Math.signum(-10));
	System.out.println(Math.sin(10));
	System.out.println(Math.sqrt(10000));
	System.out.println(Math.subtractExact(100, 10));
	System.out.println(Math.tan(10));
	System.out.println(Math.ulp(10));
	System.out.println(Math.hypot(10, 10));
	}

}
